package org.joonzis.test2;

public class Test01 {
	public static void main(String[] args) {
//		Test01.java
//		Q. 10 ~ 1 역순으로 출력하기
		
		// 1. for 문
		for (int i = 10; i >= 1; i--) {
			System.out.println(i);
		}
		
		// 2. while 문
		int num = 10;
		while(num >= 1) {
			System.out.println(num);
			num--;
		}
		
		
		
		
		
		
		
	}
}
