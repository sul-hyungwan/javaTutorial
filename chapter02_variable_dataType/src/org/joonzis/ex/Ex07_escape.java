package org.joonzis.ex;

public class Ex07_escape {
	public static void main(String[] args) {
		
		// 출력 시 옵션
		
		// 1. 줄 바꿈 : \n
		System.out.println( "\n 줄 바꿈" );
		// 2. 간격(탭) : \t
		System.out.println( "\t 간격" );
		// 3. '출력 : \'
		System.out.println( "\' 출력");
		// 4. "출력 : \"
		System.out.println( "\" 출력");
		// 5. \출력 : \\
		System.out.println( "\\ 출력");
		
	}
}





